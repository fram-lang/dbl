(** Translation of names of named parameters *)

open Common

let tr_tname (name : S.tname) =
  match name with
  | TNAnon   -> T.TNAnon
  | TNEffect -> T.TNEffect
  | TNVar x  -> T.TNVar x

let tr_name env (name : S.name) =
  match name with
  | NLabel      -> T.NLabel
  | NVar x      -> T.NVar x
  | NImplicit n -> T.NImplicit n
  | NMethod   n -> T.NMethod   n 
